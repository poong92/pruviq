#!/usr/bin/env bash
# One-shot bootstrap for the staging environment on DO.
#
# What this does (idempotent — safe to re-run):
#   1. Creates /opt/pruviq/staging/ and its subdirectories with pruviq:pruviq
#      ownership (mirrors /opt/pruviq/shared/ layout).
#   2. Writes /opt/pruviq/staging/.env from GitHub-Secret env values IF those
#      env values are non-empty in the current shell. Never overwrites
#      existing values you haven't replaced — re-running won't rotate keys.
#   3. Installs /etc/systemd/system/pruviq-api-staging.service from the
#      in-repo unit file and runs `systemctl daemon-reload`.
#
# What this intentionally does NOT do:
#   - `systemctl enable --now pruviq-api-staging` — the operator flips the
#     switch manually the first time, so they can tail logs and verify the
#     .env is correct before the service starts accepting SIGHUPs on reboot.
#   - Configure CF Tunnel ingress for staging-api.pruviq.com — that lives
#     in a separate PR because it touches Cloudflare UI state that doesn't
#     belong in this bootstrap's idempotency envelope.
#
# Intended caller: the deploy-backend.yml GitHub Actions workflow, after
# it has rsync'd a fresh /opt/pruviq/current/. Can also be invoked by
# hand: `sudo /opt/pruviq/current/backend/deploy/systemd/bin/staging-bootstrap.sh`.
#
# Required env vars (passed by GH Actions step, absent = section skipped):
#   STAGING_OKX_API_KEY, STAGING_OKX_SECRET, STAGING_OKX_PASSPHRASE
#
# 2026-04-20 initial version.

set -euo pipefail

STAGING_DIR="/opt/pruviq/staging"
REPO_ROOT="/opt/pruviq/current"
SYSTEMD_SRC="${REPO_ROOT}/backend/deploy/systemd/pruviq-api-staging.service"
SYSTEMD_DST="/etc/systemd/system/pruviq-api-staging.service"

log() { echo "[staging-bootstrap] $*"; }

# ── 1. Directory layout ──────────────────────────────────────────────────
log "creating ${STAGING_DIR} tree (idempotent)..."
install -d -o pruviq -g pruviq -m 0750 "${STAGING_DIR}"
install -d -o pruviq -g pruviq -m 0750 "${STAGING_DIR}/rankings"
# SQLite lives at ${STAGING_DIR}/autotrade_live.db — created by the app
# on first run, don't pre-touch it (the app sets correct permissions).

# Ensure the shared subscribers file exists as an empty JSON so the app
# doesn't crash on a cold start.
if [ ! -f "${STAGING_DIR}/subscribers.json" ]; then
  install -m 0640 -o pruviq -g pruviq /dev/null "${STAGING_DIR}/subscribers.json"
  echo "{}" > "${STAGING_DIR}/subscribers.json"
  chown pruviq:pruviq "${STAGING_DIR}/subscribers.json"
fi

# ── 2. .env file (only written when GH Actions supplied staging creds) ──
# Design decision: we reconstruct .env from scratch on every deploy rather
# than appending. That way a rotated key in GitHub Secrets propagates on
# the next deploy without leaving the old value around. The tradeoff is
# that if an operator hand-edits .env on the server, the next deploy wipes
# those edits — which is what we want (infra as code).
if [ -n "${STAGING_OKX_API_KEY:-}" ] \
   && [ -n "${STAGING_OKX_SECRET:-}" ] \
   && [ -n "${STAGING_OKX_PASSPHRASE:-}" ]; then
  log "writing ${STAGING_DIR}/.env from CI secrets"
  # umask so the file is created 0600 (secrets)
  umask 077
  cat > "${STAGING_DIR}/.env" <<EOF
# Auto-generated by staging-bootstrap.sh on $(date -u +%Y-%m-%dT%H:%M:%SZ)
# Do not hand-edit — this file is overwritten on every deploy.
# Source of truth: GitHub Secrets (OKX_SANDBOX_API_KEY, etc.)
OKX_API_KEY=${STAGING_OKX_API_KEY}
OKX_SECRET_KEY=${STAGING_OKX_SECRET}
OKX_PASSPHRASE=${STAGING_OKX_PASSPHRASE}
SUBSCRIBERS_FILE=${STAGING_DIR}/subscribers.json
RANKING_DIR=${STAGING_DIR}/rankings
SQLITE_DB_PATH=${STAGING_DIR}/autotrade_live.db
ENVIRONMENT=staging
EOF
  chown pruviq:pruviq "${STAGING_DIR}/.env"
  chmod 0600 "${STAGING_DIR}/.env"
else
  log "STAGING_OKX_* not provided — leaving ${STAGING_DIR}/.env untouched"
  log "  (first-run bootstrap without creds creates dirs only; CI re-run"
  log "   with secrets will populate .env)"
fi

# ── 3. systemd unit ──────────────────────────────────────────────────────
if [ ! -f "${SYSTEMD_SRC}" ]; then
  echo "::error::source unit not found at ${SYSTEMD_SRC}"
  exit 1
fi

# Only copy + reload if the unit actually changed. Keeps systemd's
# reload churn to a minimum across deploys.
if ! cmp -s "${SYSTEMD_SRC}" "${SYSTEMD_DST}" 2>/dev/null; then
  log "installing pruviq-api-staging.service (changed or missing)"
  install -o root -g root -m 0644 "${SYSTEMD_SRC}" "${SYSTEMD_DST}"
  systemctl daemon-reload
else
  log "pruviq-api-staging.service already up to date"
fi

# ── 4. Status summary (operator-facing) ──────────────────────────────────
log "bootstrap complete."
log "next manual step (first-time only):"
log "  sudo systemctl enable --now pruviq-api-staging.service"
log "  sudo journalctl -u pruviq-api-staging -n 50 --no-pager"
log "  curl -sf http://127.0.0.1:8081/health"
